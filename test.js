let colleges = {}

function addToColleges(collegeArr){
  for(let college of collegeArr){
    if(Object.keys(colleges).includes(college)){
      colleges[college] += 100-collegeArr.indexOf(college);
    }
    else{
      colleges[college] = 100-collegeArr.indexOf(college)
    }
  }
}

